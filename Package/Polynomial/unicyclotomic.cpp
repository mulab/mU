/**
\brief 位移分圆多项式的检测.
\param f 待检测的整系数多项式.
\retval {} f不是位移分圆多项式.
\retval {n,g,h} f是位移分圆多项式,且n是其阶数,g是一分圆多项式,h是一次因子,f=g(h(x)).
\note 不计\f$\pm f\f$.
\todo 整数根求解可加快
*/
uint UniShiftedCyclotomicZQ(poly_z & r,const poly_z & f)
{
	uint order=UniCyclotomicZQ_InversePhi(f);
	if(order>0)
	{
		r.resize(2);mpz_set_ui(r[0],0);mpz_set_ui(r[1],1);
		return order;
	}
	uint n=f.size()-1;
	static mpz_t a,aa,rem;
	mpz_init(a);mpz_init(aa);mpz_init(rem);
	mpz_abs(aa,f[n]);
	mpz_rootrem(a,rem,aa,n);
	if(mpz_cmp_ui(rem,0)!=0)
	{
		mpz_clear(a);mpz_clear(aa);mpz_clear(rem);
		return 0;
	}
	mpz_set(aa,a);
	poly_z _f,g,poly_temp1;
	copy_poly_z(_f,f);
	for(uint i=1;i<_f.size();i++)
	{
		if(mpz_divisible_p(_f[i],aa)!=0)
		{
			mpz_fdiv_q(_f[i],_f[i],aa);
			mpz_mul(aa,aa,a);
		}
		else
		{
			mpz_clear(a);mpz_clear(aa);mpz_clear(rem);
			_f.resize(0);
			return 0;
		}
	}
	std::vector<mpz_ptr> rootlist,rootlist1;
	mpz_add_ui(_f[0],_f[0],1);
	UniZRootZ_BySeparation(rootlist,_f);
	mpz_sub_ui(_f[0],_f[0],2);
	UniZRootZ_BySeparation(rootlist1,_f);
	mpz_add_ui(_f[0],_f[0],1);
	uint size=rootlist.size();
	for(uint j=0;j<rootlist1.size();j++)
	{
		bool flag=1;
		for(uint i=0;i<size;i++)
		{
			if(mpz_cmp(rootlist[i],rootlist1[j])==0)
			{
				flag=0;break;
			}
		}
		if(flag)
		{
			rootlist.push_back(rootlist1[j]);
			rootlist1.erase(rootlist1.begin()+j);
			j--;
		}
	}
	g.resize(2);mpz_set_ui(g[1],1);
	for(uint i=0;i<rootlist.size();i++)
	{
		mpz_set(g[0],rootlist[i]);
		uni_compose_z(poly_temp1,_f,g);
		order=UniCyclotomicZQ_InversePhi(poly_temp1);
		if(order>0)
		{
			r.resize(2);
			mpz_neg(r[0],g[0]);
			mpz_set(r[1],a);
			mpz_clear(a);mpz_clear(aa);mpz_clear(rem);
			_f.resize(0);g.resize(0);poly_temp1.resize(0);
			resize_z_list(rootlist,0);resize_z_list(rootlist1,0);
			return order;
		}
	}
	mpz_clear(a);mpz_clear(aa);mpz_clear(rem);
	_f.resize(0);g.resize(0);poly_temp1.resize(0);
	resize_z_list(rootlist,0);resize_z_list(rootlist1,0);
	return 0;
}

