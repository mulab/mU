uint nextprime(uint p)
{
	static mpz_t p_z;
	mpz_init(p_z);
	mpz_set_ui(p_z,p);
	mpz_nextprime(p_z,p_z);
	uint r=mpz_get_ui(p_z);
	mpz_clear(p_z);
	return r;
}

uint power(uint x,uint n)
{
	uint y=1,z=x;
	uint m=n;
	while(1)
	{
		if(m%2==1)
		{
			y=y*z;
			z=z*z;
		}
		else
		{
			z=z*z;
		}
		m>>=1;
		if(m==0)return y;
	}
}

void uni_compose_xn_z(poly_z & r,const poly_z & f,uint n)
{
	uint m=f.size()-1;
	uint size=m*n;
	r.resize(size+1);
	uint i=size;
	while(1)
	{
		if(i%n==0)
		{
			mpz_set(r[i],f[i/n]);
		}
		else
		{
			mpz_set_ui(r[i],0);
		}
		if(i==0)break;
		i--;
	}
	return ;
}

