uint euler_phi(uint n)
{
	if(n<=10000)return phi_table[n];
	std::vector<uint> plist;
	uint e,r;
	uint p=1;
	while(1)
	{
		p=nextprime(p);
		if(p>n)break;
		if(n%p==0)plist.push_back(p);
	}
	p=n;r=1;
	for(uint i=0;i<plist.size();i++)
	{
		e=0;
		while(p%plist[i]==0){e++;p/=plist[i];}
		r=r*(plist[i]-1)*power(plist[i],e-1);
	}
	return r;
}

